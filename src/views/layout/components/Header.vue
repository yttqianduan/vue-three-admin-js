</template>

<script>
import { reactive, toRefs } from '@vue/reactivity';

// 引入语言
import { useI18n } from "vue-i18n";
export default {
    name:"Header",
    setup(){
        // 扩展语言
        const { locale } = useI18n({ useScope: 'global' });

        // 定义数据
        const data = reactive({
            lang:[
                { label:"中文",value:"ch" },
                { label:"英文",value:"en" },
            ],
            lang_current:"ch",
        });

        // 使用i18n切换语言
        const toggleLang = (lang) => {
            locale.value = lang;
            data.lang_current = lang
        };

        return {
            data,

            toggleLang
        }
    }
}
</script>

